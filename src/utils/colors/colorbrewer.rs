//! Use one member of the `Palette` `Enum` and the requested number of colors
//! to get the corresponding color ramp with the `get_color_ramp` function.
//!
//! ```rust
//! use nannou::color::Srgb;
//! use artworks::utils::colors::colorbrewer::*;
//!
//! let ramp_orange = get_color_ramp(Palette::Oranges, 3);
//! assert_eq!(Some(vec![
//!     Srgb::new(0.996078431372549, 0.9019607843137255, 0.807843137254902),
//!     Srgb::new(0.9921568627450981, 0.6823529411764706, 0.4196078431372549),
//!     Srgb::new(0.9019607843137255, 0.3333333333333333, 0.050980392156862744),
//! ]), ramp_orange);
//! ```
//!
//! One can also get the corresponding `Palette` enum variant from it's name as a String
//! as it implements the `FromStr` trait.
//!
//! ```rust
//! use artworks::utils::colors::colorbrewer::*;
//!
//! let blue_pal: Palette = "Blues".parse().unwrap();
//! assert_eq!(blue_pal, Palette::Blues);
//! let ramp = get_color_ramp(blue_pal, 3);
//! ```
//!
//! Colors are described by their hexadecimal code.
//!
//! These color specifications and designs are developed by Cynthia Brewer
//! (http://colorbrewer2.org/).
//!
//! This implementation is a copy/paste/replace all/etc. of the code from the
//! colorbrewer-rs crate by mthh (https://github.com/mthh/colorbrewer-rs)
